// --- Friendly UTC↔local time picker for the schedule fields -------
// Admins live in different timezones; the design ships plain text inputs
// for UTC strings that are easy to mis-type. Inject:
//   - DateTimePickerUtc — wraps <input type="datetime-local"> so the
//     user picks in their local zone, with a "Now" quick-fill button.
//     Round value stays in the canonical "YYYY-MM-DD HH:MM UTC" string
//     format so nothing downstream has to change.
//   - NowDisplay — a live local + UTC clock + timezone label so the
//     admin always knows the current offset.
// And default `opens` to "now" on new-vote create.
const TIME_HELPERS = `
  function _pad2(n) { return String(n).padStart(2, "0"); }
  function _utcToLocalInput(s) {
    if (!s) return "";
    const d = new Date(s.replace(" UTC", "Z").replace(" ", "T"));
    if (isNaN(d)) return "";
    return _pad2(d.getFullYear()) + "-" + _pad2(d.getMonth()+1) + "-" + _pad2(d.getDate()) + "T" + _pad2(d.getHours()) + ":" + _pad2(d.getMinutes());
  }
  function _localInputToUtc(s) {
    if (!s) return "";
    const d = new Date(s);
    if (isNaN(d)) return "";
    return d.getUTCFullYear() + "-" + _pad2(d.getUTCMonth()+1) + "-" + _pad2(d.getUTCDate()) + " " + _pad2(d.getUTCHours()) + ":" + _pad2(d.getUTCMinutes()) + " UTC";
  }
  function _nowUtcString() {
    const d = new Date();
    return d.getUTCFullYear() + "-" + _pad2(d.getUTCMonth()+1) + "-" + _pad2(d.getUTCDate()) + " " + _pad2(d.getUTCHours()) + ":" + _pad2(d.getUTCMinutes()) + " UTC";
  }
  // Named duration presets for the F2RoundEditor close-date dropdown.
  // The "custom" key is special-cased: it lets the admin type an exact
  // close date instead of computing one from opens + days.
  const PRESET_DAYS = { "1w": 7, "2w": 14, "3w": 21, "1m": 30, "2m": 60 };
  // Add N whole days to a UTC stamp; returns "YYYY-MM-DD HH:MM UTC" or ""
  // on parse failure. Used by F2RoundEditor's duration-based close-date sync.
  function _addDaysUtc(opensUtc, days) {
    if (!opensUtc || !Number.isFinite(days)) return "";
    const d = new Date(String(opensUtc).replace(" UTC", "Z").replace(" ", "T"));
    if (isNaN(d.getTime())) return "";
    d.setUTCDate(d.getUTCDate() + days);
    return d.getUTCFullYear() + "-" + _pad2(d.getUTCMonth()+1) + "-" + _pad2(d.getUTCDate()) + " " + _pad2(d.getUTCHours()) + ":" + _pad2(d.getUTCMinutes()) + " UTC";
  }
  // Format any close-date string into a locale-aware, human-readable line.
  // Falls back to the raw input if parsing fails.
  function _prettyLocalClose(utcStr) {
    if (!utcStr) return "";
    const d = new Date(String(utcStr).replace(" UTC", "Z").replace(" ", "T"));
    if (isNaN(d.getTime())) return utcStr;
    try {
      const dateStr = d.toLocaleString(undefined, { weekday: "short", month: "short", day: "numeric", year: "numeric" });
      const timeStr = d.toLocaleString(undefined, { hour: "numeric", minute: "2-digit", timeZoneName: "short" });
      return dateStr + " · " + timeStr;
    } catch {
      return d.toISOString();
    }
  }
  // Short timezone abbreviation for the user's browser ("CET", "PDT",
  // "GMT+5:30", etc.) — surfaced in the field labels so a Madrid admin
  // sees "Opens (CET)" matching what the picker actually accepts.
  function _localTzShort() {
    try {
      const parts = new Date().toLocaleTimeString("en-US", { timeZoneName: "short" }).split(" ");
      return parts[parts.length - 1] || "local";
    } catch (e) { void e; return "local"; }
  }
  function DateTimePickerUtc({ value, onChange, disabled, placeholder }) {
    return (
      <input
        type="datetime-local"
        className="input font-mono"
        value={_utcToLocalInput(value)}
        onChange={(e) => onChange(_localInputToUtc(e.target.value))}
        disabled={disabled}
        placeholder={placeholder}
        style={{ width: "100%" }}
      />
    );
  }
  function NowDisplay() {
    const [now, setNow] = useState(new Date());
    useEffect(() => {
      const t = setInterval(() => setNow(new Date()), 30000);
      return () => clearInterval(t);
    }, []);
    const localStr = _pad2(now.getFullYear()) + "-" + _pad2(now.getMonth()+1) + "-" + _pad2(now.getDate()) + " " + _pad2(now.getHours()) + ":" + _pad2(now.getMinutes());
    const utcStr = now.getUTCFullYear() + "-" + _pad2(now.getUTCMonth()+1) + "-" + _pad2(now.getUTCDate()) + " " + _pad2(now.getUTCHours()) + ":" + _pad2(now.getUTCMinutes());
    let tz = "local";
    try { tz = Intl.DateTimeFormat().resolvedOptions().timeZone || "local"; } catch (e) { void e; }
    return (
      <div className="font-mono" style={{ fontSize: 11, color: "var(--text-muted)", marginTop: 6, lineHeight: 1.6 }}>
        Now: {localStr} ({tz}) · {utcStr} UTC
      </div>
    );
  }
`;
// Inject the helpers right above F2RoundEditor.
flow2 = flow2.replace(
  /(\s+)\/\/ ── Round editor \(create \/ edit\) ─/,
  `${TIME_HELPERS}$1// ── Round editor (create / edit) ─`,
);

